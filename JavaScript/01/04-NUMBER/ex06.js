/*
  Objectif de l'exercice : 
  Calculer `a` puissance `b`, et conserver le résultat dans la variable `result`.

  Résultat à afficher dans le terminal avant de passer à l'exercice suivant :
  8

  Explications concernant le résultat :
  Il est possible d'effectuer un calcul de puissance entre 2 variables, si elles sont toutes les deux de type Number, avec l'opérateur `**`.
  Note : 2^3 est égal à 8 car 2 * 2 * 2 = 8
*/

const a = 2;
const b = 3;

// Début de votre code
let result = a ** b;
// Fin de votre code

console.log(result); // Doit afficher `8` dans le terminal
